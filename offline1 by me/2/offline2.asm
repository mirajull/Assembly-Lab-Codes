.model small

.stack 100h

.DATA
CR EQU 0DH
LF EQU 0AH

CHAR DB ?
PREV DB ?
NEXT DB ?

.CODE

MAIN PROC

MOV AX,@DATA
MOV DS,AX

;INPUT A CHARACTER AND CONVERT TO UPPER CASE
MOV AH,1
INT 21H
SUB AL,20H
SUB AL,01H
MOV PREV,AL
ADD AL,02H
MOV NEXT,AL  

MOV AH,2
MOV DL,0DH  ;CARRIAGE RETURN
INT 21H
MOV DL,0AH  ;LINE FEED
INT 21H

MOV AH,2
MOV DL,PREV
INT 21H

MOV AH,2
MOV DL,NEXT
INT 21H              
         
;TERMINATION         
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN

